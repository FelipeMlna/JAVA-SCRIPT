let names = "Ilya";

// Las comillas inertidas tienen como funcion insertar dentro de ${} en la cadena, en esto caso es el 1.
alert( `Hello ${1}` ); // hello 1

// "name" Es la expresión que está dentro de la cadena.
alert( `Hello ${"Monica"}` ); // hello name

// Le expresión puesta en este caso es la variable nombrada anteriormente en la parte superior "names"
alert( `Hello ${names}` ); // hello Ilya